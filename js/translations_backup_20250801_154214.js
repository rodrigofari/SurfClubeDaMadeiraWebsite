// Translation function
function translatePage(lang) {
  if (!translations[lang]) return;
  
  // Update all translatable elements
  const elements = document.querySelectorAll('[data-translate]');
  elements.forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[lang][key]) {
      // Check if the translation contains HTML (like links)
      const translationHasHTML = translations[lang][key].includes('<a') || translations[lang][key].includes('<b') || translations[lang][key].includes('<i');
      if (translationHasHTML) {
        // Use innerHTML for elements with HTML content
        element.innerHTML = translations[lang][key];
      } else {
        // Use textContent for plain text
        element.textContent = translations[lang][key];
      }
    }
  });
  
  // Update language dropdown button text (desktop and mobile)
  const languageNames = {
    'en': 'EN', 'pl': 'PL', 'it': 'IT', 'de': 'DE', 'fr': 'FR', 'pt': 'PT', 'es': 'ES', 'uk': 'UK', 'fi': 'FI', 'sv': 'SV', 'ru': 'RU'
  };
  const displayName = languageNames[lang] || lang.toUpperCase();
  
  // Desktop
  const desktopDropdown = document.querySelector('#languageDropdown');
  if (desktopDropdown) {
    desktopDropdown.innerHTML = `<i class="bi bi-translate"></i> ${displayName}`;
  }
  
  // Mobile
  const mobileDropdown = document.querySelector('#languageDropdownMobile');
  if (mobileDropdown) {
    mobileDropdown.innerHTML = `<i class="bi bi-translate"></i> ${displayName}`;
  }
  
  // Store language preference
  localStorage.setItem('preferred-language', lang);
}

// Initialize translation system
function initializeTranslation() {
  // Load saved language preference or default to 'en'
  let savedLang = localStorage.getItem('preferred-language');
  
  if (!savedLang || !translations[savedLang]) {
    // Try to detect browser language if not set
    const browserLang = (navigator.language || navigator.userLanguage || 'en').slice(0, 2);
    savedLang = translations[browserLang] ? browserLang : 'en';
    localStorage.setItem('preferred-language', savedLang);
  }
  
  // Apply translation immediately
  translatePage(savedLang);

  // Add event delegation for language dropdown items (works for both existing and dynamically added elements)
  document.addEventListener('click', function(e) {
    if (e.target && e.target.hasAttribute('data-lang')) {
      e.preventDefault();
      const lang = e.target.getAttribute('data-lang');
      if (translations[lang]) {
        translatePage(lang);
      }
    }
  });
}

// Initialize translation system when DOM is ready
document.addEventListener('DOMContentLoaded', initializeTranslation); 